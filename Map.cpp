#include <limits>
#include "Map.h"

float Map::GetCost(int srcRow, int srcCol, int dstRow, int dstCol) const
{
	if (srcRow < 0 || srcRow >= height ||
		srcCol < 0 || srcCol >= width ||
		dstRow < 0 || dstRow >= height ||
		dstCol < 0 || dstCol >= width)
		return std::numeric_limits<float>::infinity();

	if (GetElement(srcRow, srcCol) == '#' || GetElement(dstRow, dstCol) == '#')
		return std::numeric_limits<float>::infinity();
	else
		return 1.0f;
}


